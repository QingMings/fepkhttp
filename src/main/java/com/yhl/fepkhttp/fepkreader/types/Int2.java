package com.yhl.fepkhttp.fepkreader.types;

public class Int2 {

    public Int2() {
    }

    public Int2(int x, int y) {
      this.x = x;
      this.y = y;
    }

    private int x = 0;
    private int y = 0;

    public int getX() {
      return x;
    }

    public void setX(int x) {
      this.x = x;
    }

    public int getY() {
      return y;
    }

    public void setY(int y) {
      this.y = y;
    }
  }
